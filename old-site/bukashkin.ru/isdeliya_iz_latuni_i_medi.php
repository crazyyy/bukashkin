<p>Буквально с 1988-го года наше предприятие начало работать над таким процессом, как художественная ковка. <strong>Подсвечники из меди и латуни</strong>, светильники и многое другое – все это постепенно внедрялось в процессе работы. И в итоге, мы пришли к тому, что на сегодняшний день можем похвастаться широчайшим ассортиментом, репутацией, а также массой постоянных клиентов. </p>

<p>Прекрасные <strong>изделия из меди</strong>, которые купить вы сможете у нас по довольно-таки приемлемой цене, мы изготавливаем любой сложности. Подвесные бра и светильники, петли, лэйблы, декоративные вставки, ручки, <strong>подсвечники латунные и медные</strong>, а также многие другие декоративные элементы из металла – все это прекрасно подойдет для того, чтобы украсить, например, ресторан, кафе или даже интерьер своего дома. </p>

<p>Такие <strong>изделия из меди</strong>, производство которых отличается внедрением особых инновационных технологий на фоне привычных и старинных стандартов итальянских и испанских мастеров, представлены также в нашем каталоге подставками, подносами, флюгерами, пепельницами, кувшинами, козырьками, чайниками и т д.</p>

<p>Такого плана <strong>изделия из латуни можно купить и под заказ</strong>. То есть наши мастера изготовят для вас изделие нужного именно вам размера  и дизайна. Это немаловажное преимущество нашей работы. Именно за счет хорошей пластичности применяемого металла получается изготавливать декоративные изделия абсолютно любых форм и размеров. Любой предмет такого рода станет удивительным и неповторимым презентом, который вовсе никто не будет ожидать. </p>


